public class DataTypes {
    public static void main (String[] args){
        byte b1=21;
        short Shortname = 19999;
        int i = 150;
        long LongNumber=4556;
        float FloatNumber=32.3f;
        double D1=100.00;
        boolean B1 = true;
        char c1='D';
        System.out.println("Byte is :" + b1);
        System.out.println("Shortname is :" + Shortname);
        System.out.println("i is :" + i);
        System.out.println("LongNumber is :" + LongNumber);
        System.out.println("FloatNumber is :" + FloatNumber);
        System.out.println("Double is :" + D1);
        System.out.println("Boolean is :" + B1);
        System.out.println("Char is :" + c1);




    }
}
